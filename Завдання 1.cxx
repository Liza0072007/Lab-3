#include <iostream> 
using namespace std; 

int main() { 
    
    int number; // Змінна для збереження числа, яке введе користувач
    
    cout << "Введіть число від 1 до 999: "; // Запит числа у користувача
    
    cin >> number; // Зчитування введеного значення
    
    // Перевіряємо, чи число у межах 1–999
    if (number < 1 || number > 999) { 
        cout << "Число поза діапазоном 1-999." << endl; // Повідомлення про невідповідність
        return 1; // Завершення програми, якщо число неправильне
    }
    
    // Визначаємо, парне чи непарне число 
    string parity = (number % 2 == 0) ? "парне" : "непарне";

    string digits; // Змінна для опису кількості цифр у числі

    // Визначення, скільки цифр у числі
    if (number < 10)
        digits = "однозначне"; // Число менше 10 — це однозначне число
    else if (number < 100)
        digits = "двозначне"; // Число менше 100 — це двозначне
    else
        digits = "тризначне"; // Інакше — тризначне

    // Вивід результатів: парність і кількість цифр
    cout << parity << " " << digits << " число" << endl;
   
    return 0; 
}
